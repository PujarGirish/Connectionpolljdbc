package com.ty.stastnory;

public class Laptop {
	private int Id;
	private String Brand;
	private int Ram;
	private String Processor;
	private int Rom;

	public int getId() {
		return Id;
	}

	public void setId(int id) {
		Id = id;
	}

	public String getBrand() {
		return Brand;
	}

	public void setBrand(String brand) {
		Brand = brand;
	}

	public int getRam() {
		return Ram;
	}

	public void setRam(int ram) {
		Ram = ram;
	}

	public String getProcessor() {
		return Processor;
	}

	public void setProcessor(String processor) {
		Processor = processor;
	}

	public int getRom() {
		return Rom;
	}

	public void setRom(int rom) {
		Rom = rom;
	}

}
