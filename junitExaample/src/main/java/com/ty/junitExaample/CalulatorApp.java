public class CalulatorApp {
public int  add(int value1,int value2) {
	return value1+value2;
}
public int  sub(int value1,int value2) {
	return value1-value2;
}
public int  mull(int value1,int value2) {
	return value1*value2;
}
public int  div(int value1,int value2) {
	return value1/value2;
}

}

